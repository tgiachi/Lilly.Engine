namespace Lilly.Engine.Rendering.Core.Data.Input;

/// <summary>
/// Represents the state of a button (pressed or released).
/// </summary>
public enum ButtonState
{
    /// <summary>
    /// The button is released.
    /// </summary>
    Released = 0,

    /// <summary>
    /// The button is pressed.
    /// </summary>
    Pressed = 1
}
